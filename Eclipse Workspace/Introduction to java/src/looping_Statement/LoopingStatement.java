package looping_Statement;

public class LoopingStatement {

	public static void main(String[] args) {
		/*
		 *for loop
		 *whil loop
		 *do-whil loop
		 *for each loop 
		 * */
		for(int i = 1; i <= 100; i++) {
		System.out.println("Indu A");

	}
		
		//while loop;
		
		int i = 1;
        while (i <= 5) {
            System.out.println("hello: " + i);
            i++;
        }
        
        
        //do while loop
        int l = 1;
        do {
            System.out.println("Iteration" );
            l++;
        } while (l <= 5);
       
	}
	
}
	


